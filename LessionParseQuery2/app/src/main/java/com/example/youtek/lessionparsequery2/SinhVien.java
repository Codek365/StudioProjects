public class SinhVien {
    public String HoTen;
    public Integer NamSinh;
    public Integer Toan;
    public Integer Ly;
    public Integer Hoa;
    public Integer Tong;

    public SinhVien(String hoTen, Integer namSinh, Integer toan, Integer ly, Integer hoa, Integer tong) {
        HoTen = hoTen;
        NamSinh = namSinh;
        Toan = toan;
        Ly = ly;
        Hoa = hoa;
        Tong = tong;
    }
}
